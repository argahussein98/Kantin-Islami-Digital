package com.example.android.kantinislamidigital.Data;

public class DataCendikia {
    private String id;
    private String tanggal;
    private String nama_penulis;
    private String judul;
    private String email;
    private String text;
    private String gambar;

    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }


    public String getTanggal() {
        return tanggal;
    }
    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }


    public String getNamaPenulis() {
        return nama_penulis;
    }
    public void setNamaPenulis(String nama_penulis) {
        this.nama_penulis = nama_penulis;
    }


    public String getJudul() {
        return judul;
    }
    public void setJudul(String judul) {
        this.judul = judul;
    }


    public String getEmail() {
        return email;
    }
    public void setEmail(String email) {
        this.email = email;
    }


    public String getTextual() {
        return text;
    }
    public void setTextual(String text) {
        this.text = text;
    }


    public String getGambar() {
        return gambar;
    }
    public void setGambar(String gambar) {
        this.gambar = gambar;
    }

}
